export const DEFAULT_CUSTOM_SUBTYPES = {
  'Custom Surface Work': ['Standard', 'Premium', 'Luxury'],
  'Custom Linear Work': ['Standard', 'Heavy Duty', 'Decorative'],
  'Custom Unit Work': ['Single', 'Multiple', 'Complex'],
};

export const DEFAULT_CUSTOM_DEFAULT_SUBTYPES = {
  'Custom Surface Work': 'Standard',
  'Custom Linear Work': 'Standard', 
  'Custom Unit Work': 'Single',
};

// Helper functions
export const getAllWorkTypes = () => {
  const allTypes = [];
  Object.values(WORK_TYPES).forEach(categoryTypes => {
    allTypes.push(...categoryTypes);
  });
  return allTypes;
};

export const getWorkTypeCategory = (workType) => {
  for (const [category, types] of Object.entries(WORK_TYPES)) {
    if (types.includes(workType)) {
      return category;
    }
  }
  return null;
};

export const isCategoryValid = (categoryKey) => {
  return !!(categoryKey && (WORK_TYPES[categoryKey] || categoryKey.startsWith('custom_')));
};

export const getCategoryWorkTypes = (categoryKey) => {
  if (!categoryKey) return [];
  
  // Handle custom categories
  if (categoryKey.startsWith('custom_')) {
    return DEFAULT_CUSTOM_WORK_TYPES;
  }
  
  // Handle predefined categories
  return WORK_TYPES[categoryKey] || [];
};

export const getSubtypeOptions = (workType) => {
  if (!workType?.trim()) return null;
  
  // Check predefined subtypes first
  if (SUBTYPE_OPTIONS[workType]) {
    return [...SUBTYPE_OPTIONS[workType]];
  }
  
  // Check custom subtypes
  if (DEFAULT_CUSTOM_SUBTYPES[workType]) {
    return [...DEFAULT_CUSTOM_SUBTYPES[workType]];
  }
  
  return null;
};

export const getDefaultSubtype = (workType) => {
  if (!workType?.trim()) return null;
  
  // Check predefined defaults first
  if (DEFAULT_SUBTYPES[workType]) {
    return DEFAULT_SUBTYPES[workType];
  }
  
  // Check custom defaults
  if (DEFAULT_CUSTOM_DEFAULT_SUBTYPES[workType]) {
    return DEFAULT_CUSTOM_DEFAULT_SUBTYPES[workType];
  }
  
  return null;
};